"use client"

import { useEffect, useState } from "react"
import { useQuery } from "@tanstack/react-query"
import { Brain, TrendingUp, AlertCircle, Lightbulb, RefreshCw, Loader2, Sparkles, Target, Clock } from "lucide-react"
import clsx from "clsx"
import { useToast } from "@/components/ui/toast"

interface PredictionResult {
  patterns: string[]
  predictions: string[]
  recommendations: string[]
}

function PatternCard({ icon, title, items, color }: { icon: React.ReactNode; title: string; items: string[]; color: string }) {
  if (items.length === 0) return null
  return (
    <div className="glass-card p-card-padding space-y-3">
      <div className="flex items-center gap-2">
        <div className={clsx("w-8 h-8 rounded-xl flex items-center justify-center", color)}>
          {icon}
        </div>
        <h3 className="text-sm font-bold text-on-surface">{title}</h3>
        <span className="text-[10px] font-mono text-on-surface-variant/50 ml-auto">{items.length}</span>
      </div>
      <div className="space-y-2">
        {items.map((item, i) => (
          <div key={i} className="flex items-start gap-2 px-3 py-2 rounded-xl bg-surface-variant/20">
            <div className="w-1.5 h-1.5 rounded-full bg-current mt-1.5 shrink-0 opacity-40" />
            <p className="text-xs text-on-surface/80 leading-relaxed">{item}</p>
          </div>
        ))}
      </div>
    </div>
  )
}

export default function PredictionsPage() {
  useEffect(() => { document.title = "Predictions — LUMARY Studio" }, [])
  const { toast } = useToast()

  const { data, isLoading, error, refetch } = useQuery<PredictionResult>({
    queryKey: ["predictions"],
    queryFn: () => fetch("/api/v1/agent/predict").then((r) => r.json()),
    refetchInterval: 300000,
  })

  const [refreshKey, setRefreshKey] = useState(0)

  if (error) {
    return (
      <div className="flex items-center justify-center min-h-[400px] animate-fadeIn">
        <div className="text-center">
          <div className="w-16 h-16 rounded-2xl bg-error-container border border-error/20 flex items-center justify-center mx-auto mb-4">
            <AlertCircle className="w-8 h-8 text-error" />
          </div>
          <h3 className="text-lg font-semibold text-on-surface mb-1">Analysis unavailable</h3>
          <p className="text-sm text-on-surface-variant/70 mb-6">Run the cron scan or check your data first</p>
          <button onClick={() => refetch()} className="inline-flex items-center gap-2 px-5 py-2.5 rounded-xl bg-primary text-on-primary text-sm font-semibold hover:bg-primary-fixed-dim transition-all">
            <RefreshCw className="w-4 h-4" /> Retry
          </button>
        </div>
      </div>
    )
  }

  return (
    <div className="space-y-6 animate-fadeIn">
      {/* Header */}
      <div className="flex items-center justify-between">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 rounded-xl bg-tertiary/10 flex items-center justify-center">
            <Brain className="w-5 h-5 text-tertiary" />
          </div>
          <div>
            <h1 className="text-2xl font-heading font-bold text-on-surface">Predictive Analysis</h1>
            <p className="text-sm text-on-surface-variant/80 mt-0.5">
              Cross-domain patterns, predictions, and recommendations generated by AI
            </p>
          </div>
        </div>
        <button
          onClick={() => { refetch(); toast("success", "Refreshing") }}
          disabled={isLoading}
          className="inline-flex items-center gap-2 px-4 py-2 rounded-xl bg-surface-variant/50 text-on-surface-variant text-xs font-semibold hover:bg-surface-variant transition-all disabled:opacity-40"
        >
          {isLoading ? <Loader2 className="w-4 h-4 animate-spin" /> : <RefreshCw className="w-4 h-4" />}
          Refresh
        </button>
      </div>

      {/* Stats bar */}
      <div className="grid grid-cols-2 md:grid-cols-4 gap-3">
        {[
          { label: "Patterns Found", value: data?.patterns?.length || 0, icon: <Target className="w-4 h-4" />, color: "bg-purple-100 text-purple-700 dark:bg-purple-900/30 dark:text-purple-300" },
          { label: "Predictions", value: data?.predictions?.length || 0, icon: <TrendingUp className="w-4 h-4" />, color: "bg-blue-100 text-blue-700 dark:bg-blue-900/30 dark:text-blue-300" },
          { label: "Recommendations", value: data?.recommendations?.length || 0, icon: <Lightbulb className="w-4 h-4" />, color: "bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-300" },
          { label: "Last Updated", value: "Just now", icon: <Clock className="w-4 h-4" />, color: "bg-emerald-100 text-emerald-700 dark:bg-emerald-900/30 dark:text-emerald-300" },
        ].map((stat) => (
          <div key={stat.label} className="glass-card p-card-padding flex items-center gap-3">
            <div className={clsx("w-9 h-9 rounded-xl flex items-center justify-center shrink-0", stat.color)}>
              {stat.icon}
            </div>
            <div>
              <p className="text-xs text-on-surface-variant/70">{stat.label}</p>
              <p className="text-lg font-bold text-on-surface">{typeof stat.value === "number" ? stat.value : stat.value}</p>
            </div>
          </div>
        ))}
      </div>

      {isLoading ? (
        <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
          {[1, 2, 3].map((i) => (
            <div key={i} className="glass-card p-card-padding space-y-4">
              <div className="h-4 w-32 rounded-xl bg-gradient-to-r from-surface-container-highest via-surface-container to-surface-container-highest bg-[length:200%_100%] animate-shimmer" />
              <div className="space-y-2">
                {[1, 2, 3].map((j) => (
                  <div key={j} className="h-10 rounded-xl bg-gradient-to-r from-surface-container-highest via-surface-container to-surface-container-highest bg-[length:200%_100%] animate-shimmer" />
                ))}
              </div>
            </div>
          ))}
        </div>
      ) : (
        <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">
          <PatternCard
            icon={<Target className="w-4 h-4" />}
            title="Patterns"
            items={data?.patterns || []}
            color="bg-purple-100 text-purple-700 dark:bg-purple-900/30 dark:text-purple-300"
          />
          <PatternCard
            icon={<TrendingUp className="w-4 h-4" />}
            title="Predictions"
            items={data?.predictions || []}
            color="bg-blue-100 text-blue-700 dark:bg-blue-900/30 dark:text-blue-300"
          />
          <PatternCard
            icon={<Lightbulb className="w-4 h-4" />}
            title="Recommendations"
            items={data?.recommendations || []}
            color="bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-300"
          />
        </div>
      )}

      {/* Empty state */}
      {!isLoading && !data?.patterns?.length && !data?.predictions?.length && !data?.recommendations?.length && (
        <div className="glass-card p-card-padding flex flex-col items-center justify-center py-20 text-center">
          <div className="w-16 h-16 rounded-2xl bg-surface-variant/50 border border-outline-variant/30 flex items-center justify-center mb-5">
            <Brain className="w-7 h-7 text-on-surface-variant/80" />
          </div>
          <h3 className="text-base font-semibold text-on-surface mb-1.5">No insights yet</h3>
          <p className="text-sm text-on-surface-variant/70 max-w-sm mb-6">
            The AI needs more data to find patterns. Keep using the studio — track habits, log payments, update projects.
          </p>
        </div>
      )}

      {/* Explanation */}
      <div className="rounded-xl bg-gradient-to-r from-tertiary/5 to-transparent border border-tertiary/20 p-5">
        <div className="flex items-start gap-3">
          <div className="w-10 h-10 rounded-xl bg-tertiary/10 flex items-center justify-center shrink-0">
            <Sparkles className="w-5 h-5 text-tertiary" />
          </div>
          <div className="text-sm text-on-surface-variant/80 leading-relaxed">
            <strong className="text-on-surface">How it works:</strong> Your business and personal data is analyzed by Gemini AI every time you refresh. It looks for connections between your habits, projects, payments, goals, and focus time — then predicts what might happen next and what you should do about it. The more data you feed it, the sharper the insights get.
          </div>
        </div>
      </div>
    </div>
  )
}
